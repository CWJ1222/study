package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100;
        double b = 10.5; // 실수
        boolean c = true; //true false 만 가능
        char d = 'A'; // 문자 하나
        String e = "Hello Java"; //문자열등

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
